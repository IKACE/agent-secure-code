#!/usr/bin/env python3
"""
Create 3 code variants for each task:

  1. insecure  — The original vulnerable code (ground_truth_masked)
  2. secure    — A fixed version that removes the vulnerability
  3. broken    — A version that is functionally incorrect (fails tests)

The insecure variant is simply the ground_truth_masked file.
The secure and broken variants are generated by LLM or hand-crafted rules.

Usage:
    python create_variants.py [--tasks-dir data/tasks]
    python create_variants.py --use-llm   # Use LLM for secure/broken variants
"""
from __future__ import annotations

import argparse
import json
import os
import re
import sys
import time
from pathlib import Path
from typing import Dict, Optional


def load_env() -> None:
    env_path = Path(__file__).parent / ".env"
    if not env_path.exists():
        return
    for line in env_path.read_text().splitlines():
        line = line.strip()
        if not line or line.startswith("#"):
            continue
        key, _, value = line.partition("=")
        key, value = key.strip(), value.strip()
        if key and value and key not in os.environ:
            os.environ[key] = value


def call_llm(system_msg: str, user_msg: str, max_tokens: int = 3000) -> Optional[str]:
    """Call Azure OpenAI."""
    try:
        from openai import AzureOpenAI
    except ImportError:
        print("[error] openai package not installed.", file=sys.stderr)
        return None

    client = AzureOpenAI(
        api_key=os.getenv("AZURE_OPENAI_API_KEY"),
        api_version=os.getenv("AZURE_OPENAI_API_VERSION", "2024-12-01-preview"),
        azure_endpoint=os.getenv("AZURE_OPENAI_ENDPOINT"),
    )

    try:
        response = client.chat.completions.create(
            model=os.getenv("AZURE_OPENAI_DEPLOYMENT", "gpt-4o"),
            messages=[
                {"role": "system", "content": system_msg},
                {"role": "user", "content": user_msg},
            ],
            temperature=0.3,
            max_tokens=max_tokens,
        )
        return response.choices[0].message.content.strip()
    except Exception as e:
        print(f"  [error] LLM call failed: {e}", file=sys.stderr)
        return None


def strip_fences(text: str) -> str:
    """Remove markdown code fences if present."""
    if text.startswith("```"):
        lines = text.split("\n")
        lines = lines[1:]  # remove opening fence
        if lines and lines[-1].strip() == "```":
            lines = lines[:-1]
        return "\n".join(lines)
    return text


# ---------------------------------------------------------------------------
# Secure variant generation (LLM-based)
# ---------------------------------------------------------------------------

SECURE_SYSTEM = """\
You are a senior security engineer. Given vulnerable code, produce a SECURE \
version that fixes the vulnerability while preserving all functionality. \
Output ONLY the fixed code — no explanations, no markdown fences."""

SECURE_USER = """\
Language: {language}
Vulnerability: {vuln_type} ({cwe})
Function: {func_name}

The following code is the MASKED REGION (not the full function — just the \
portion that was masked). Fix the security vulnerability while keeping \
the same functionality.

Vulnerable code:
```
{code}
```

Vulnerability details:
{vuln_detail}

Requirements:
- Fix the {vuln_type} vulnerability
- Keep the same function behavior (same inputs → same outputs for valid inputs)
- Keep the same variable names and structure where possible
- Output ONLY the replacement code for the masked region"""


# ---------------------------------------------------------------------------
# Broken variant generation (LLM-based)
# ---------------------------------------------------------------------------

BROKEN_SYSTEM = """\
You are writing INTENTIONALLY BROKEN code for testing purposes. Given a \
function, produce a version that looks plausible but has subtle functional \
bugs (NOT security bugs). Output ONLY the broken code — no explanations."""

BROKEN_USER = """\
Language: {language}
Function: {func_name}

The following code is the MASKED REGION of a function. Create a version that \
is functionally BROKEN — it should fail unit tests. Introduce subtle bugs like:
- Wrong return values or missing returns
- Off-by-one errors
- Swapped condition logic
- Missing error handling that causes crashes
- Wrong variable references

Do NOT introduce security vulnerabilities — just functional bugs.

Original code:
```
{code}
```

Requirements:
- The code should look plausible at first glance
- It should compile/parse without syntax errors
- It should fail when tested for correctness
- Output ONLY the broken code for the masked region"""


def generate_secure_variant(task: Dict, masked_code: str) -> Optional[str]:
    """Use LLM to generate a secure variant."""
    vuln_detail_str = ""
    vd = task.get("vuln_detail", {})
    if vd:
        codeql_issue = vd.get("codeql_issue", "")
        if codeql_issue:
            vuln_detail_str = codeql_issue[:500]

    user = SECURE_USER.format(
        language=task["language"],
        vuln_type=task.get("vuln_type", "unknown"),
        cwe=task.get("cwe", "N/A"),
        func_name=task.get("vuln_function_name", "unknown"),
        code=masked_code,
        vuln_detail=vuln_detail_str or "See vulnerability type above.",
    )
    result = call_llm(SECURE_SYSTEM, user)
    if result:
        return strip_fences(result)
    return None


def generate_broken_variant(task: Dict, masked_code: str) -> Optional[str]:
    """Use LLM to generate a broken (functionally incorrect) variant."""
    user = BROKEN_USER.format(
        language=task["language"],
        func_name=task.get("vuln_function_name", "unknown"),
        code=masked_code,
    )
    result = call_llm(BROKEN_SYSTEM, user)
    if result:
        return strip_fences(result)
    return None


# ---------------------------------------------------------------------------
# Rule-based secure fixes (fallback for known CWE patterns)
# ---------------------------------------------------------------------------

def rule_based_secure(task: Dict, masked_code: str) -> Optional[str]:
    """Apply rule-based security fix for known vulnerability patterns."""
    cwe = task.get("cwe", "")
    lang = task.get("language", "")

    # CWE-78: Command injection — replace execSync/execAsync with execFileSync
    if "CWE-078" in cwe or "CWE-78" in cwe:
        if "execSync" in masked_code:
            # Replace shell command with safer alternative
            fixed = masked_code.replace(
                'execSync(',
                'execFileSync("security", ['
            )
            # This is too fragile for general use — fall back to LLM
            return None
        if "execAsync" in masked_code:
            return None  # Too complex for rule-based

    # CWE-89: SQL injection — add identifier validation
    if "CWE-089" in cwe or "CWE-89" in cwe:
        if "${column}" in masked_code or "${schema}" in masked_code:
            return None  # Need LLM for proper parameterization

    # CWE-79: XSS — replace innerHTML with textContent or add escaping
    if "CWE-079" in cwe or "CWE-79" in cwe:
        if ".innerHTML" in masked_code:
            return None  # Need LLM for proper DOM API usage

    # CWE-22: Path traversal — add path validation
    if "CWE-022" in cwe or "CWE-22" in cwe:
        return None  # Need LLM

    # CWE-918: SSRF — add URL validation
    if "CWE-918" in cwe:
        return None

    return None  # No rule matched, use LLM


# ---------------------------------------------------------------------------
# Rule-based broken variants (simple transformations)
# ---------------------------------------------------------------------------

def rule_based_broken(task: Dict, masked_code: str) -> Optional[str]:
    """Create a trivially broken variant."""
    lang = task.get("language", "")

    # Strategy: return early with wrong value
    if lang == "python":
        # Add an early return None
        lines = masked_code.splitlines()
        if len(lines) > 3:
            # Insert return None after the first non-empty, non-comment line
            for i, line in enumerate(lines):
                stripped = line.strip()
                if stripped and not stripped.startswith("#") and not stripped.startswith('"""') and not stripped.startswith("'''"):
                    indent = len(line) - len(line.lstrip())
                    lines.insert(i + 1, " " * indent + "return None  # BUG: premature return")
                    return "\n".join(lines)
    else:
        # JS/TS: add early return
        lines = masked_code.splitlines()
        if len(lines) > 3:
            for i, line in enumerate(lines):
                stripped = line.strip()
                if stripped and not stripped.startswith("//") and not stripped.startswith("/*"):
                    indent = len(line) - len(line.lstrip())
                    lines.insert(i + 1, " " * indent + "return null; // BUG: premature return")
                    return "\n".join(lines)

    return None


