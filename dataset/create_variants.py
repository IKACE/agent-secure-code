# Main
# ---------------------------------------------------------------------------

def create_variants_for_task(task_dir: Path, task: Dict, use_llm: bool = True) -> Dict:
    """Create insecure/secure/broken variants for one task."""
    variants_dir = task_dir / "variants"
    variants_dir.mkdir(exist_ok=True)

    # Detect extension
    ext = "txt"
    for e in ("ts", "tsx", "js", "jsx", "mjs", "py", "html"):
        if (task_dir / f"ground_truth_masked.{e}").exists():
            ext = e
            break

    # Read the masked code (= insecure variant)
    masked_file = task_dir / f"ground_truth_masked.{ext}"
    if not masked_file.exists():
        return {"error": f"ground_truth_masked.{ext} not found"}

    masked_code = masked_file.read_text(encoding="utf-8")

    # 1. Insecure variant = original vulnerable code
    insecure_file = variants_dir / f"insecure.{ext}"
    insecure_file.write_text(masked_code, encoding="utf-8")

    result = {"insecure": str(insecure_file)}

    # 2. Secure variant
    secure_code = None
    if use_llm:
        secure_code = rule_based_secure(task, masked_code)
        if not secure_code:
            secure_code = generate_secure_variant(task, masked_code)
    if not secure_code:
        secure_code = rule_based_secure(task, masked_code)

    if secure_code:
        secure_file = variants_dir / f"secure.{ext}"
        secure_file.write_text(secure_code, encoding="utf-8")
        result["secure"] = str(secure_file)
    else:
        result["secure_error"] = "Could not generate secure variant"

    # 3. Broken variant
    broken_code = None
    if use_llm:
        broken_code = generate_broken_variant(task, masked_code)
    if not broken_code:
        broken_code = rule_based_broken(task, masked_code)

    if broken_code:
        broken_file = variants_dir / f"broken.{ext}"
        broken_file.write_text(broken_code, encoding="utf-8")
        result["broken"] = str(broken_file)
    else:
        result["broken_error"] = "Could not generate broken variant"

    return result


def main() -> None:
    load_env()

    ap = argparse.ArgumentParser(description="Create code variants for tasks")
    ap.add_argument("--tasks-dir", type=Path,
                    default=Path(__file__).parent / "data" / "tasks")
    ap.add_argument("--tasks-jsonl", type=Path,
                    default=Path(__file__).parent / "data" / "tasks_function_level.jsonl")
    ap.add_argument("--use-llm", action="store_true", default=True,
                    help="Use LLM for secure/broken variants (default: True)")
    ap.add_argument("--no-llm", action="store_true",
                    help="Use only rule-based variants")
    ap.add_argument("--force", action="store_true",
                    help="Overwrite existing variants")
    args = ap.parse_args()

    use_llm = args.use_llm and not args.no_llm

    if use_llm:
        required = ["AZURE_OPENAI_API_KEY", "AZURE_OPENAI_ENDPOINT"]
        missing = [k for k in required if not os.getenv(k)]
        if missing:
            print(f"[error] Missing env vars: {', '.join(missing)}", file=sys.stderr)
            sys.exit(1)

    # Load tasks from JSONL for full metadata
    tasks_by_id = {}
    if args.tasks_jsonl.exists():
        with open(args.tasks_jsonl, encoding="utf-8") as f:
            for line in f:
                line = line.strip()
                if line:
                    task = json.loads(line)
                    tasks_by_id[task["task_id"]] = task

    task_dirs = sorted(d for d in args.tasks_dir.iterdir() if d.is_dir())
    print(f"Found {len(task_dirs)} task directories.", file=sys.stderr)

    for i, task_dir in enumerate(task_dirs, 1):
        task_id = task_dir.name

        # Skip if variants exist and not forcing
        if not args.force and (task_dir / "variants").exists():
            existing = list((task_dir / "variants").glob("*"))
            if len(existing) >= 3:
                print(f"  [{i}/{len(task_dirs)}] {task_id[:55]} SKIP (exists)", file=sys.stderr)
                continue

        task = tasks_by_id.get(task_id)
        if not task:
            task_json = task_dir / "task.json"
            if task_json.exists():
                task = json.loads(task_json.read_text(encoding="utf-8"))
            else:
                print(f"  [{i}/{len(task_dirs)}] {task_id[:55]} SKIP (no metadata)", file=sys.stderr)
                continue

        print(f"  [{i}/{len(task_dirs)}] {task_id[:55]}...",
              file=sys.stderr, end=" ", flush=True)

        result = create_variants_for_task(task_dir, task, use_llm=use_llm)

        status = []
        if "insecure" in result:
            status.append("insecure")
        if "secure" in result:
            status.append("secure")
        if "broken" in result:
            status.append("broken")
        print(f"{', '.join(status)}", file=sys.stderr)

        if use_llm:
            time.sleep(0.5)

    print(f"\nDone creating variants for {len(task_dirs)} tasks.", file=sys.stderr)


if __name__ == "__main__":
    main()
